     * Store a newly created resource in storage.
     */
    public function store(Request $request)
    {
        $request->validate([
            'name' => 'required|string|max:255|unique:benefits,name',
        ]);

        $benefit = Benefit::create([
            'name' => $request->name,
        ]);

        return response()->json([
            'success' => true,
            'benefit' => [
                'id' => $benefit->id,
                'name' => $benefit->name,
            ],
        ]);
    }

